"""
Problem:
Demonstrate loop constructs in Python.

Approach:
- Use a for loop to iterate a fixed number of times
- Use a while loop with a condition
- Show basic iteration logic clearly

Time Complexity:
O(n)

Space Complexity:
O(1)
"""

# For loop example
print("For loop output:")
for i in range(5):
    print(i)

# While loop example
print("\nWhile loop output:")
count = 1
while count <= 5:
    print(count)
    count += 1
