"""
Problem:
Demonstrate the use of basic operators in Python.

Approach:
- Use arithmetic, comparison, logical, and assignment operators
- Display results clearly to show operator behavior

Time Complexity:
O(1)

Space Complexity:
O(1)
"""

a = 10
b = 3

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulo:", a % b)

# Comparison Operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

# Logical Operators
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

# Assignment Operators
a += 5
print("a after += 5:", a)
